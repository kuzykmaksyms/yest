public class Task {
    public static void main(String[] args) {

        //Write your solution here
        for (char c = 'A'; c <= 'Z'; c++) {
            System.out.print(c);
        }
        System.out.println();

        for (char c = 'z'; c >= 'a'; c--) {
            System.out.print(c);
        }
        System.out.println();
    }
}